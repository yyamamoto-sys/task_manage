// UIラベル定数とsystemPrompt向けUIガイドを一元管理するモジュール。
// ★ ボタン名を変更するときはこのファイルだけ直す。
//    UIコンポーネントとsystemPromptが同じ定数を参照するため、乖離が発生しない。
//
// 使い方：
//   コンポーネント側 → BTN_* 定数をそのまま表示文字列として使う
//   systemPrompt側  → UI_GUIDE_SECTION を BASE_SYSTEM に埋め込む

/** 新規PJ提案カードのボタン（押すと編集・確認ダイアログが開く） */
export const BTN_CONFIRM_CREATE = "確認して作成";

/** 確認ダイアログの確定ボタン（押すとDBに反映される） */
export const BTN_APPLY_CONFIRMED = "確定して反映";

/** 通常提案カードの反映ボタン（add_project 以外すべて） */
export const BTN_APPLY = "反映する";

/** シフト一括適用ボタンのラベル（shift_days が設定された提案のみ表示） */
export const btnShift = (days: number) =>
  `全て +${days}日シフト（AIの提案に揃える）`;

/**
 * systemPrompt に埋め込む UIガイドセクション。
 * 提案カードのボタン名を正確にAIに伝え、「確認する」等の
 * 存在しないボタンを案内しないようにする。
 */
export const UI_GUIDE_SECTION = `
## UIガイド（ボタン名の正式名称）

AIがユーザーに操作手順を案内するときは、下表の正式ボタン名のみを使うこと。
存在しないボタン名（「確認する」「OK」「作成する」等）は絶対に使わないこと。

| 場面 | ボタン名 | 補足 |
|------|----------|------|
| 新規PJ提案カード | 「${BTN_CONFIRM_CREATE}」 | 押すと編集・確認ダイアログが開く |
| 確認ダイアログ | 「${BTN_APPLY_CONFIRMED}」 | 押すとDBに反映される（最終確定） |
| 日程・担当変更などの提案カード | 「${BTN_APPLY}」 | 変更内容をDBに反映する |
| 一括日程シフト | 「全て +N日シフト」 | shift_days付き提案のみ表示。N=実際の日数 |
`.trim();

/**
 * systemPrompt に埋め込む機能一覧セクション。
 * ★ 機能を追加・削除・変更したときはこの関数を更新すること（CLAUDE.md Section 17）。
 */
export const FEATURE_LIST_SECTION = `
## 現在利用可能な機能一覧

ユーザーから「○○できますか？」「○○はどこにありますか？」と聞かれたときに参照すること。

### ビュー（画面）
- ダッシュボード：PJ選択で今週のタスク・KR進捗・期限アラートを表示
- ガントチャート：PJ別／人別で横軸タイムライン。PJのタスク一覧を折りたたみ可
- リストビュー：タスク一覧をPJ別・担当者別・タグ別にグループ表示
- カンバンビュー：ステータスごとの列でタスクカードをドラッグ移動
- カレンダービュー：月間カレンダーでタスク期日・マイルストーンを確認

### タスク機能
- タスクに「親タスク」を設定することで2階層の親子構造を作れる
- タスクにタグ（自由入力）を付けて分類・絞り込み可能
- 担当者は複数設定可（共同担当）
- 工数（見積もり時間）を入力でき、メンバーごとの工数サマリーをAIが参照可能
- 開始日と期日の両方を設定可能
- タスクに@メンション（@メンバー名）を入力すると通知が届く
- ドラッグ操作でタスクの並び順を変更可能

### プロジェクト機能
- プロジェクトの作成・名前変更・色変更は管理画面またはガントビューの✏️ボタンから
- プロジェクトにオーナー・メンバー・開始日・終了日を設定可能

### AI相談（5モード）
- 変更の影響確認：タスク変更がPJ全体に与える影響を分析
- What-ifシミュレーション：仮の変更案を試して影響を見る（DBに反映しない）
- 現状診断：現在のプロジェクト状況を俯瞰して課題を抽出
- 締め切り逆算：期日から逆算してタスクスケジュールを提案
- スコープ変更：PJ停止・タスク削減の影響を分析

### OKR管理
- Objective / KR（主要な成果指標）/ TF（タスクフォース）の3階層管理
- KRセッション記録（チェックイン・ウィンセッション・自由形式）
- KRレポート自動生成
`.trim();
